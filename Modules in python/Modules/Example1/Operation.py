# Modules in Python
# Operation.py ----> File Name and Module Name
def Mulop(b, c):
    a = b * c
    print("Multiplication of ({},{}) = {}".format(b, c, a))
def Subop(b, c):
    a = b - c
    print("Subtraction of ({},{}) = {}".format(b, c, a))

def Sumop(b, c):
    a = b + c
    print("Addition of ({},{}) = {}".format(b, c, a))
