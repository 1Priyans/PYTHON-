# Modules in Python
# Operation.py ----> File Name and Module Name (Module_Operation_Ex1.py in folder Python2.0----> Module_operation)
def Mulop(b, c):
    a = b * c
    print("Multiplication of ({},{}) = {}".format(b, c, a))
def Subop(b, c):
    a = b - c
    print("Subtraction of ({},{}) = {}".format(b, c, a))

def Sumop(b, c):
    a = b + c
    print("Addition of ({},{}) = {}".format(b, c, a))
