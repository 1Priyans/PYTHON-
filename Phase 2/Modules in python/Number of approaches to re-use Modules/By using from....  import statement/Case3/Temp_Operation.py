def CtoK():
    C = float(input("Enter the Value Converted into Kelvin: "))
    C_to_K = C + 273.15
    print("Value of {} in Kelvin is {}".format(C,C_to_K))
def CtoF():
    C = float(input("Enter the Value Converted into Fahrenheit: "))
    C_to_F = C * (9/5) + 32
    print("Value of {} in Fahrenheit is {}".format(C, C_to_F))
